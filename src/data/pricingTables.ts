// Helper to get premium
// Updated to accept gender (default to 'male' if not provided)
export const getVHISPremium = (productId: string, age: number, variant: string, gender: string = 'male'): number => {
  let table: PricingTable | undefined;
  
  // Normalize gender
  const isFemale = gender.toLowerCase() === 'female';

  if (productId === 'AVHIF') {
    // AVHIF Logic with Gender
    if (variant === 'Ward') {
      table = isFemale ? AVHIF_FEMALE_WARD_DATA : AVHIF_MALE_WARD_DATA;
    } else if (variant === 'Semi-Private') {
      // Points to the new Semi-Private table
      table = isFemale ? AVHIF_FEMALE_SEMIPRIVATE_DATA : AVHIF_MALE_SEMIPRIVATE_DATA;
    } else if (variant === 'Private') {
      // Points to the corrected Private table
      table = isFemale ? AVHIF_FEMALE_PRIVATE_DATA : AVHIF_MALE_PRIVATE_DATA;
    } else {
      // Default to Ward if unknown
      table = isFemale ? AVHIF_FEMALE_WARD_DATA : AVHIF_MALE_WARD_DATA;
    }
  } else if (productId === 'AVHIP') {
    table = AVHIP_PRICING[variant] || AVHIP_ASIA_0;
  } else if (productId === 'AVHIS') {
    // For AVHIS, gender doesn't matter as per user instruction "Important Note on Gender: ... does not distinguish between Male and Female"
    // So we just ignore gender and use the variant.
    table = AVHIS_PRICING[variant] || AVHIS_PRICING['HKD 0'];
  } else if (productId === 'AVHISS') {
    // AVHISS (Standard Scheme) differentiates by gender
    table = isFemale ? AVHISS_FEMALE_STANDARD : AVHISS_MALE_STANDARD;
  }

  if (!table) return 0;

  // 1. Exact age match
  if (table[age]) return table[age];

  // 2. Interpolation (Linear) for missing years
  const ages = Object.keys(table).map(Number).sort((a, b) => a - b);
  
  // Handle out of bounds
  if (age < ages[0]) return table[ages[0]];
  if (age > ages[ages.length - 1]) return table[ages[ages.length - 1]];

  // Find lower and upper bounds
  let lowerAge = ages[0];
  let upperAge = ages[ages.length - 1];
  
  for (let i = 0; i < ages.length - 1; i++) {
    if (ages[i] <= age && ages[i+1] >= age) {
      lowerAge = ages[i];
      upperAge = ages[i+1];
      break;
    }
  }

  if (lowerAge === upperAge) return table[lowerAge];

  const lowerVal = table[lowerAge];
  const upperVal = table[upperAge];
  
  // Linear interpolation formula
  const fraction = (age - lowerAge) / (upperAge - lowerAge);
  return Math.round(lowerVal + (upperVal - lowerVal) * fraction);
};
